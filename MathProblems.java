
// Calculating the Area of a Rectangle

public class MathProblems{
	public static void main(String[] args) {
		
				int a = 5, b = 2;
				int areaRectangle = a * b;
				
				//print 
				System.out.println("The Area of a Rectangle: " + areaRectangle);
				
				// a more polished version of Area of a Rectangle:
				// Create integer variables
				int length = 5;
				int width = 2;
				int area;
				
				// Calculate the area of a rectangle 
				area = length + width;
				
				// Print variables 
				System.out.println("Length is: " + length);
				System.out.println("Width is: " + width);
				System.out.println("Area of the rectangle is: " + area);
				
				
				/* based on the above exercise 
				 * Calculating the parameter of a rectangle
				 */
				
				
				// Create integer variables 
				int l = 9; // VariableName - lenght (l)
				int w = 15; // VariableName - width (w)
				int parameter;
				
				// Calculate the parameter of the rectangle
				parameter = 2 * (l + w);
				
				// Print variables 
				System.out.println("Length is: " + l);
				System.out.println("Width is: " + w);
				System.out.println("Parameter of a rectangle is: " + parameter);
							
				
	}
}